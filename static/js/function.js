function alert_stock(name, stock) {
    const Toast = Swal.mixin({
        toast: true,
        position: "top-end",
        showConfirmButton: false,
        timer: 3000,
        timerProgressBar: true,
        didOpen: (toast) => {
            toast.onmouseenter = Swal.stopTimer;
            toast.onmouseleave = Swal.resumeTimer;
        }
    });
    Toast.fire({
        icon: "error",
        title: "Lleg√≥ al limite de su stock <strong style='color: red'> " + name + "</strong> solo tiene " + "<strong>(" + stock + ")</strong>",
    });
}